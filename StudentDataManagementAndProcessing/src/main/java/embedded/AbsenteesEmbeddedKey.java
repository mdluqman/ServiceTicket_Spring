package embedded;

import java.io.Serializable;

public class AbsenteesEmbeddedKey implements Serializable{

	private static final long serialVersionUID = 1L;

	private String username;
	private int subject_ID;
	private String date; 
	
	public AbsenteesEmbeddedKey() {
	}

	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + ((date == null) ? 0 : date.hashCode());
		result = prime * result + subject_ID;
		result = prime * result + ((username == null) ? 0 : username.hashCode());
		return result;
	}

	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		AbsenteesEmbeddedKey other = (AbsenteesEmbeddedKey) obj;
		if (date == null) {
			if (other.date != null)
				return false;
		} else if (!date.equals(other.date))
			return false;
		if (subject_ID != other.subject_ID)
			return false;
		if (username == null) {
			if (other.username != null)
				return false;
		} else if (!username.equals(other.username))
			return false;
		return true;
	}
	
	
}
